#include <iostream>
#include <cmath>

int main() 
{
	std::cout << "Absolute value of 0.025 is " << abs(+0.025) << std::endl;

	std::cout << "Absolute value of -1.62 is " << abs(-1.62) << std::endl;

	std::cout << "Square root of 25 is " << sqrt(25) << std::endl;

	std::cout << "Square root of 16 is " << sqrt(16) << std::endl;

	return 0;
}